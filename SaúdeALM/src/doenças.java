import java.util.Date;

public class doenças {
	private String Id_doenças;
	private String PeriodoInicial;
	private String Término;
	private Date Data;
	public String getId_doenças() {
		return Id_doenças;
	}
	public void setId_doenças(String id_doenças) {
		Id_doenças = id_doenças;
	}
	public String getPeriodoInicial() {
		return PeriodoInicial;
	}
	public void setPeriodoInicial(String periodoInicial) {
		PeriodoInicial = periodoInicial;
	}
	public String getTérmino() {
		return Término;
	}
	public void setTérmino(String término) {
		Término = término;
	}
	public Date getData() {
		return Data;
	}
	public void setData(Date data) {
		Data = data;
	}
	


	

}
